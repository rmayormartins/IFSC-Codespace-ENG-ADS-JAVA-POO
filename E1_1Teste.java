//CÓDIGO E1_1 - Operadores aritméticos em Java:
public class E1_1Teste {
    public static void main(String[] args) {
        int a = 21;
        int b = 10;
        int c;
        
        c = a + b; // soma
        System.out.println("Valor de c: " + c);
        c = a - b; // subtração
        System.out.println("Valor de c: " + c);
        c = a * b; // multiplicação
        System.out.println("Valor de c: " + c);
        c = a / b; // divisão
        System.out.println("Valor de c: " + c);
        c = a % b; // resto
        System.out.println("Valor de c: " + c);
    }
 }