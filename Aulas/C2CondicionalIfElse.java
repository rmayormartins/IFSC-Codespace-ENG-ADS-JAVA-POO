package Aulas;
public class C2CondicionalIfElse {
    public static void main(String[] args) {
        int a = 100;
        
        if (a < 20) {
            // SE a for menor
            System.out.println("a menor que 20");
        } else {
            // SENAO
            System.out.println("a nao eh menor que 20");
        }
    }
}
